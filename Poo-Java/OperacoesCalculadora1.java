
public class OperacoesCalculadora1 {
	
	public double adicao (double numero1, double numero2) { //COM RETORNO//
		double resultado;
		resultado = numero1+numero2; //REALIZANDO A OPERAÇÃO DE SOMA//
		return resultado; //RETORNANDO O RESULTADO//
	}
	
	public double subtracao(double numero1, double numero2) {
		double resultado;
		resultado = numero1-numero2;
		return resultado;	
		
	}
	
	public double divisao(double numero1, double numero2) {
		double resultado;
		resultado = numero1/numero2;
		return resultado;
	}
	
	public double multiplicacao(double numero1, double numero2) {
		double resultado;
		resultado = numero1*numero2;
		return resultado;
	}

}
