
public class Calculadora4 {

	private int num1, num2;
	
		public void setNum1(int numero1){
		
			this.num1 = numero1;
	}

		public int getNum1 () {
		
			return this.num1;
	}

		public void setNum2(int numero2){
		
			this.num2 = numero2;
	}

		public int getNum2 (){
		
			return this.num2;
	}

		public int Soma(){
		
			return num1+num2;
	}

		public int Subtr(){
		
			return num1-num2;
	}

		public int Divisao(){
			
			return num1/num2;
	}

		public int Multi(){
		
			return num1*num2;
	}
	
	
}
